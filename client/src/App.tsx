import "./App.css";
import { useEffect, useRef, useState } from "react";
import { Game } from "./game/Game";
import { Board } from "./components/board";
import { BoardContext } from "./contexts/board-context";
import { HostPanel } from "./components/host-panel";
import { TGame } from "./types/game.type";
import { Popup } from "./components/popup";
import { SharePanel } from "./components/share-panel";

function App() {
    const [connection, setConnection] = useState(true);
    const [board, dispatch] = useState<TGame>(() => ({
        players: [],
        status: "initial",
        me: null,
        host: null,
        turn: null,
        bet: null,
    }));

    const [isHostPanelVisible, setIsHostPanelVisible] = useState(false);
    const [isSharePanelVisible, setIsSharePanelVisible] = useState(false);
    const game = useRef<Game>();

    useEffect(() => {
        const g = new Game();

        g.onStateUpdate = (msg) => {
            console.log(msg);
            dispatch(msg);
        };

        g.onOpen = () => {
            setConnection(true);
        };

        g.onClose = () => {
            setConnection(false);
            console.log("Connection closed");
        };

        game.current = g;

        return () => {
            g.destroy();
        };
    }, []);

    function handleIAmHost(): void {
        setIsHostPanelVisible(true);
        game.current?.iAmHost();
    }

    return (
        <>
            <BoardContext.Provider value={board}>
                <div className="content">
                    <Board
                        game={game.current}
                        handleIAmHost={() => handleIAmHost()}
                    ></Board>
                </div>

                {!connection && (
                    <div className="no-connection">
                        No connection, reload the page
                        <button onClick={() => window.location.reload()}>
                            <svg
                                xmlns="http://www.w3.org/2000/svg"
                                width="16"
                                height="16"
                                fill="currentColor"
                                viewBox="0 0 16 16"
                            >
                                <path
                                    fillRule="evenodd"
                                    d="M8 3a5 5 0 1 0 4.546 2.914.5.5 0 0 1 .908-.417A6 6 0 1 1 8 2z"
                                />
                                <path d="M8 4.466V.534a.25.25 0 0 1 .41-.192l2.36 1.966c.12.1.12.284 0 .384L8.41 4.658A.25.25 0 0 1 8 4.466" />
                            </svg>
                        </button>
                    </div>
                )}

                {board.me && board.me === board.host && isHostPanelVisible && (
                    <Popup onClose={() => setIsHostPanelVisible(false)}>
                        <HostPanel
                            game={game.current}
                            onClose={() => setIsHostPanelVisible(false)}
                        ></HostPanel>
                    </Popup>
                )}

                {isSharePanelVisible && (
                    <Popup onClose={() => setIsSharePanelVisible(false)}>
                        <SharePanel></SharePanel>
                    </Popup>
                )}

                <div className="actions">
                    <div className="actions__left">
                        {import.meta.env.DEV && (
                            <div>
                                <button
                                    onClick={() => {
                                        console.log(board);
                                        console.log(board.players);
                                    }}
                                >
                                    <svg
                                        xmlns="http://www.w3.org/2000/svg"
                                        width="16"
                                        height="16"
                                        fill="currentColor"
                                        viewBox="0 0 16 16"
                                    >
                                        <path d="M4.355.522a.5.5 0 0 1 .623.333l.291.956A5 5 0 0 1 8 1c1.007 0 1.946.298 2.731.811l.29-.956a.5.5 0 1 1 .957.29l-.41 1.352A5 5 0 0 1 13 6h.5a.5.5 0 0 0 .5-.5V5a.5.5 0 0 1 1 0v.5A1.5 1.5 0 0 1 13.5 7H13v1h1.5a.5.5 0 0 1 0 1H13v1h.5a1.5 1.5 0 0 1 1.5 1.5v.5a.5.5 0 1 1-1 0v-.5a.5.5 0 0 0-.5-.5H13a5 5 0 0 1-10 0h-.5a.5.5 0 0 0-.5.5v.5a.5.5 0 1 1-1 0v-.5A1.5 1.5 0 0 1 2.5 10H3V9H1.5a.5.5 0 0 1 0-1H3V7h-.5A1.5 1.5 0 0 1 1 5.5V5a.5.5 0 0 1 1 0v.5a.5.5 0 0 0 .5.5H3c0-1.364.547-2.601 1.432-3.503l-.41-1.352a.5.5 0 0 1 .333-.623M4 7v4a4 4 0 0 0 3.5 3.97V7zm4.5 0v7.97A4 4 0 0 0 12 11V7zM12 6a4 4 0 0 0-1.334-2.982A3.98 3.98 0 0 0 8 2a3.98 3.98 0 0 0-2.667 1.018A4 4 0 0 0 4 6z" />
                                    </svg>
                                </button>
                            </div>
                        )}
                    </div>
                    <div className="actions__right">
                        <button onClick={() => setIsSharePanelVisible(true)}>
                            <svg
                                xmlns="http://www.w3.org/2000/svg"
                                width="16"
                                height="16"
                                fill="currentColor"
                                viewBox="0 0 16 16"
                            >
                                <path d="M13.5 1a1.5 1.5 0 1 0 0 3 1.5 1.5 0 0 0 0-3M11 2.5a2.5 2.5 0 1 1 .603 1.628l-6.718 3.12a2.5 2.5 0 0 1 0 1.504l6.718 3.12a2.5 2.5 0 1 1-.488.876l-6.718-3.12a2.5 2.5 0 1 1 0-3.256l6.718-3.12A2.5 2.5 0 0 1 11 2.5m-8.5 4a1.5 1.5 0 1 0 0 3 1.5 1.5 0 0 0 0-3m11 5.5a1.5 1.5 0 1 0 0 3 1.5 1.5 0 0 0 0-3" />
                            </svg>
                        </button>
                        {board.me && board.me === board.host && (
                            <button onClick={() => setIsHostPanelVisible(true)}>
                                <svg
                                    xmlns="http://www.w3.org/2000/svg"
                                    width="16"
                                    height="16"
                                    fill="currentColor"
                                    viewBox="0 0 16 16"
                                >
                                    <path d="M8 4.754a3.246 3.246 0 1 0 0 6.492 3.246 3.246 0 0 0 0-6.492M5.754 8a2.246 2.246 0 1 1 4.492 0 2.246 2.246 0 0 1-4.492 0" />
                                    <path d="M9.796 1.343c-.527-1.79-3.065-1.79-3.592 0l-.094.319a.873.873 0 0 1-1.255.52l-.292-.16c-1.64-.892-3.433.902-2.54 2.541l.159.292a.873.873 0 0 1-.52 1.255l-.319.094c-1.79.527-1.79 3.065 0 3.592l.319.094a.873.873 0 0 1 .52 1.255l-.16.292c-.892 1.64.901 3.434 2.541 2.54l.292-.159a.873.873 0 0 1 1.255.52l.094.319c.527 1.79 3.065 1.79 3.592 0l.094-.319a.873.873 0 0 1 1.255-.52l.292.16c1.64.893 3.434-.902 2.54-2.541l-.159-.292a.873.873 0 0 1 .52-1.255l.319-.094c1.79-.527 1.79-3.065 0-3.592l-.319-.094a.873.873 0 0 1-.52-1.255l.16-.292c.893-1.64-.902-3.433-2.541-2.54l-.292.159a.873.873 0 0 1-1.255-.52zm-2.633.283c.246-.835 1.428-.835 1.674 0l.094.319a1.873 1.873 0 0 0 2.693 1.115l.291-.16c.764-.415 1.6.42 1.184 1.185l-.159.292a1.873 1.873 0 0 0 1.116 2.692l.318.094c.835.246.835 1.428 0 1.674l-.319.094a1.873 1.873 0 0 0-1.115 2.693l.16.291c.415.764-.42 1.6-1.185 1.184l-.291-.159a1.873 1.873 0 0 0-2.693 1.116l-.094.318c-.246.835-1.428.835-1.674 0l-.094-.319a1.873 1.873 0 0 0-2.692-1.115l-.292.16c-.764.415-1.6-.42-1.184-1.185l.159-.291A1.873 1.873 0 0 0 1.945 8.93l-.319-.094c-.835-.246-.835-1.428 0-1.674l.319-.094A1.873 1.873 0 0 0 3.06 4.377l-.16-.292c-.415-.764.42-1.6 1.185-1.184l.292.159a1.873 1.873 0 0 0 2.692-1.115z" />
                                </svg>
                            </button>
                        )}
                    </div>
                </div>
            </BoardContext.Provider>
        </>
    );
}

export default App;
